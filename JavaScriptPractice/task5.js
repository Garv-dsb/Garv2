// switch statement is like the multiple if else statement

const grade = prompt("Enter your Grade:");

switch (grade) {
  case "A":
    alert("Congratulations You Got Grade A");
    break;
  case "B":
    alert("Congratulations You Got Grade B");
    break;
  case "C":
    alert("Congratulations You Got Grade C");
    break;
  case "D":
    alert("Congratulations You Got Grade D");
    break;

  default:
    alert("You Fail!!");
    break;
}
